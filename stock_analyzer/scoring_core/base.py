    return TomorrowScorer().score(
        df,
        top_n=top_n,
        market_filter=market_filter,
        market_regime=market_regime,
        display_cap=display_cap,
        expected_return_samples=expected_return_samples,
        use_expected_return_ranking=use_expected_return_ranking,
        capture_candidate_pool=capture_candidate_pool,
    )


