def score_today_candidates(
    df: pd.DataFrame,
    hot_ranks: Dict[str, int],
    industry_strength: Dict[str, float],
    sentiment_lookup: Dict[str, Dict[str, object]],
    top_n: int = 10,
    market_filter: str = "all",
    market_regime: Dict[str, object] = None,
    capture_candidate_pool: bool = False,
    scoring_context: Dict[str, object] = None,
) -> Tuple[Dict[str, List[Dict[str, object]]], Dict[str, object]]:
    from ..strategies.today import TodayScorer

    return TodayScorer().score(
        df,
        hot_ranks,
        industry_strength,
        sentiment_lookup,
        top_n=top_n,
        market_filter=market_filter,
        market_regime=market_regime,
        capture_candidate_pool=capture_candidate_pool,
        scoring_context=scoring_context,
    )


